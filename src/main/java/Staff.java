
// Staff class representing a staff, with fields name, address, phone, email and
// salary
public class Staff {
   
    public Staff(String staffID, String name, String address, String phone, String email, double salary) {
        super();
        this.staffID = staffID;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.salary = salary;
    }
    String staffID;
    String name;
    String address;
    String phone;
    String email;
    double salary;
}
